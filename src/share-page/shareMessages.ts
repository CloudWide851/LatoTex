import type { ShareDevice, ShareI18n, ShareLocale } from "./shareTypes";

function normalizeLocale(raw: string | null | undefined): ShareLocale {
  const value = String(raw ?? "").trim().toLowerCase();
  return value.startsWith("zh") ? "zh-CN" : "en-US";
}

export function detectShareLocale(preferred?: string | null): ShareLocale {
  if (preferred) {
    return normalizeLocale(preferred);
  }
  return normalizeLocale(typeof navigator === "undefined" ? "" : (navigator.language || navigator.languages?.[0] || ""));
}

export function detectShareDevice(preferred?: string | null): ShareDevice {
  const normalizedPreferred = String(preferred ?? "").trim().toLowerCase();
  if (normalizedPreferred === "mobile" || normalizedPreferred === "desktop") {
    return normalizedPreferred;
  }
  const userAgent = typeof navigator === "undefined" ? "" : String(navigator.userAgent || "").toLowerCase();
  return /android|iphone|ipad|ipod|mobile|windows phone/i.test(userAgent) ? "mobile" : "desktop";
}

